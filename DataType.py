#Integers and floats are both numbers, but they are different types of numbers. Integers are whole numbers, while floats are numbers that can have a decimal point.
age = 30
height = 1.75
print("Age:", age)
print("Height:", height)

#Strings are sequences of characters. They can be enclosed in single quotes, double quotes, or triple quotes.
name = "Alice"
greeting = 'Hello, ' + name + '!'
print(greeting)
#Booleans are a type of data that can only have two values: True or False. They are often used in conditional statements to control the flow of a program.
is_student = True
is_employed = False
print("Is student:", is_student)
print("Is employed:", is_employed)

#Lists are ordered collections of items. They can contain items of different types, and they are mutable, meaning you can change their contents after they are created.
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)
#Dictionaries are collections of key-value pairs. They are unordered and mutable. Each key must be unique, and it is used to access the corresponding value.
person = {"name": "Alice", "age": 30, "city": "New York"}
print("Person:", person)

coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

print("Type of age:", type(age))
print("Type of height:", type(height)) 
print("Type of name:", type(name))
print("Type of is_student:", type(is_student))
print("Type of fruits:", type(fruits))
print("Type of person:", type(person))
print("Type of coordinates:", type(coordinates))
